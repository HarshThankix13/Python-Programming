# control statements :
    
#     1) conditional statement
#     2) looping statements
#     3) jumping statements
    
#     1) conditional statements :
#         -> if statements (it will execute when condition goes true )
        
#         syntax :
#             if condition:
#                 statements
    
#     -> if... else statement : (It Will Execute When Condition Goes True or False)
        
#         syntax:
#             if condition:
#                 true statement
#             else: false statements
    
#     -> elif statements : (for Multiple condition)
#             syntax:
#                 if condition:
#                     statements
#                 elif condition:
#                     statements
#                 elif condition:
#                     statements
#                 else:
#                     statements
                    
#     -> nested if statements (if inside that if statements)
    
#     syntax:
#         if condition:
#             statements
#             if condition:
#                 statements
#             else:
#                 statements
#             else:
#                 statements