# How can you use variables to store the results of mathematical operations in Python?
# Mathematical operations using variables
a = 5
b = 3

# Addition
sum_result = a + b

# Subtraction
difference = a - b

# Multiplication
product = a * b

# Division
quotient = a / b

# Modulo (remainder)
remainder = a % b

# Display the results
print("Sum:", sum_result)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
print("Remainder:", remainder)
