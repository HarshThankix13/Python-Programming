# How can you access characters in a string using indexing and store them in variables?
# Accessing characters in a string using indexing
my_string = "Hello, World!"

# Access the first character
first_char = my_string[0]

# Access the last character
last_char = my_string[-1]

# Access a specific character in the middle
middle_char = my_string[7]

# Display the extracted characters
print("First character:", first_char)
print("Last character:", last_char)
print("Middle character:", middle_char)
